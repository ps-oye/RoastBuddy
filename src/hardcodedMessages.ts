/**
 * Get a random roast message for the given intensity.
 * Replaces {lines} placeholder with actual count.
 */
export function getRandomRoast(intensity: RoastIntensity, linesAdded: number, language: MessageLanguage = 'english'): string {
    const messages = ROAST_MESSAGES[language][intensity];
    const index = Math.floor(Math.random() * messages.length);
    return messages[index].replace('{lines}', String(linesAdded));
}

/**
 * Get a random cheer message.
 */
export function getRandomCheer(language: MessageLanguage = 'english'): string {
    const index = Math.floor(Math.random() * CHEER_MESSAGES[language].length);
    return CHEER_MESSAGES[language][index];
}
